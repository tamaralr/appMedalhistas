class Atleta{
	private nome: string;
	private idade: number;
	private peso: number;
	private altura: number;
	private sexo: string;
	private dopping: boolean;

	constructor(nome: string, idade: number, peso:number, altura: number, sexo: string, dopping: boolean){
		this.nome = nome;
		this.idade = idade;
		this.peso = peso;
		this.altura = altura
		this.sexo = sexo;
		this.dopping = dopping;
	}


}
